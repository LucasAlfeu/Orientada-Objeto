public class Aluno {
    private int id;
    private String nomeAluno;
    private Curso cursoId; // Estamos criando uma associação entre a classe Aluno e a classe Curso, esse atributo é a chave estrangeira

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNomeAluno() {
        return nomeAluno;
    }

    public void setNomeAluno(String nomeAluno) {
        this.nomeAluno = nomeAluno;
    }

    public Curso getCursoId() {
        return cursoId;
    }

    public void setCursoId(Curso cursoId) {
        this.cursoId = cursoId;
    }
    
    
}
